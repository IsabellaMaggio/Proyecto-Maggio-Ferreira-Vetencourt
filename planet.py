#clase de planetas
class Planet():
    def __init__(self, name, orbital_period, rotation_period, population, climate, films, residents, url):
        self.name = name
        self.orbital_period = orbital_period
        self.rotation_period = rotation_period
        self.population = population
        self.climate = climate
        self.films = films
        self.residents = residents
        self.url = url